export function buildPrefixIndex(): Map<string, DictionaryEntry[]> {
  const index = new Map<string, DictionaryEntry[]>();

  for (const entry of dictionary) {
    for (let len = 1; len <= Math.min(4, entry.word.length); len++) {
      const prefix = entry.word.slice(0, len);
      let list = index.get(prefix);
      if (!list) {
        list = [];
        index.set(prefix, list);
      }
      list.push(entry);
    }
  }

  // Sort each prefix bucket by frequency descending
  for (const [, list] of index) {
    list.sort((a, b) => b.frequency - a.frequency);
  }

  return index;
}
